/**
 * @file for_1000.c
 * @brief prints the numbers 1 through 1000, one number per line 
 *
 * @author Matthew Olsen
 * @date September 19th 2015 
 */

#include <stdio.h>

int main(void)
{
        int i;
        for (i = 1; i <=1000; i++) {
                printf("%d\n", i);
        }

        return 0;
}
